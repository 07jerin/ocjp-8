package com.jerin.oracle.certification.common;

public class PrintUtil {

	public static void spacer() {
		System.out.println();
		System.out.println("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++");
		System.out.println("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++");
		System.out.println();
	}
	
	public static void spacer(String message) {
		System.out.println();
		System.out.println("+++++++++++++++++  "+ message +"  ++++++++++++++++++++++++");
		System.out.println("++++++++++++++++++++++++++++++++++++++++++++++++++++++++++");
		System.out.println();
	}

}
